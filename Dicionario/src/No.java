
public class No {
	private int k; // chave
	private Object o; // objeto valor
	private boolean available;
	
	public int getK() {
		return k;
	}
	public void setK(int k) {
		this.k = k;
	}
	public Object getO() {
		return o;
	}
	public void setO(Object o) {
		this.o = o;
	}
	public boolean isAvailable() {
		return available;
	}
	public void setAvailable(boolean available) {
		this.available = available;
	}
	
}
